# Time Complexity Analysis

# Function: fac(n)
# This function calculates factorial of n .
# Each call subtracting n by 1 and performs one multiplication.
# Total calls = n
# => Time Complexity: n

# Function: print1to10(n)
# This function prints numbers from n to 10 recursively.
# At most 10 calls, each doing a simple print.
# => Time Complexity: 10